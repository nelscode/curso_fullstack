// Var e Let: Primeira diferença

var nome = 'nelson'; // declarando
console.log(nome)

var nome = 'martins'; // Com o var, nos podemos redeclarar uma variavel
console.log(nome)

nome = 'nelson' // podemos criar variaveis assim também, MAS NÃO FAÇA ISSO
// isso é uma variavel global

let sobrenome = 'martins' // o let corrige isso no ECMASCRIPT (ES6)
// let sobrenome = 'martins'  com o let não podemos, isso causará um erro

// Não use var, use let!